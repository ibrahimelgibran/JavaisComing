public class AugmenetedAssignments {

    public static void main(String[] args) {

        int c = 100;

        c += 10; // Menambahkan 10 ke nilai c (c = c + 10)
        System.out.println(c);

        c -= 10; // Mengurangkan 10 dari nilai c (c = c - 10)
        System.out.println(c);

        c *= 10; // Mengalikan nilai c dengan 10 (c = c * 10)
        System.out.println(c);

        // unary operator
        int d = 100;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);
      
    }
}
